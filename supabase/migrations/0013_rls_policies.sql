-- Remaining RLS policies (kept together here because several reference
-- tables that only exist once the full schema above has been created).
--
-- Shape used throughout:
--   - chu_hui / pho_chu_hui: full read+write on organizational data.
--   - ke_toan: full read, write limited to payments/bids (their job).
--   - thanh_vien: read-only, scoped to rows tied to their own member_id.
--   - Tables mutated only via close_period()/open_period() (periods,
--     chain_shares winner fields, period_bid_distributions) get select-only
--     policies here — writes bypass RLS because those functions are owned
--     by the migration role, which Postgres exempts from RLS by default.

-- hui_chains ------------------------------------------------------------
create policy hui_chains_select on hui_chains for select
  to authenticated
  using (
    current_role_name() in ('chu_hui', 'pho_chu_hui', 'ke_toan')
    or exists (
      select 1 from chain_members cm
      where cm.chain_id = hui_chains.id and cm.member_id = my_member_id()
    )
  );

create policy hui_chains_insert on hui_chains for insert
  to authenticated
  with check (current_role_name() in ('chu_hui', 'pho_chu_hui'));

create policy hui_chains_update on hui_chains for update
  to authenticated
  using (current_role_name() in ('chu_hui', 'pho_chu_hui'))
  with check (current_role_name() in ('chu_hui', 'pho_chu_hui'));

create policy hui_chains_delete on hui_chains for delete
  to authenticated
  using (current_role_name() in ('chu_hui', 'pho_chu_hui'));

