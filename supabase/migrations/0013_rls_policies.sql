-- notifications -----------------------------------------------------------
create policy notifications_select on notifications for select
  to authenticated
  using (profile_id = auth.uid() or profile_id is null);

create policy notifications_update on notifications for update
  to authenticated
  using (profile_id = auth.uid() or profile_id is null)
  with check (profile_id = auth.uid() or profile_id is null);

create policy notifications_insert on notifications for insert
  to authenticated
  with check (current_role_name() in ('chu_hui', 'pho_chu_hui', 'ke_toan'));

-- activity_log (every server action logs its own actor) -------------------
create policy activity_log_select on activity_log for select
  to authenticated
  using (true);

create policy activity_log_insert on activity_log for insert
  to authenticated
  with check (actor_id = auth.uid());
